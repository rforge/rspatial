"holepoly1" <-
structure(list(), pts = list(structure(list(x = c(1, 4, 4, 1), 
    y = c(4, 4, 1, 1), hole = FALSE), .Names = c("x", "y", "hole"
)), structure(list(x = c(2, 3, 3, 2), y = c(2, 2, 3, 3), hole = TRUE), .Names = c("x", 
"y", "hole"))), class = structure("gpc.poly", package = "gpclib"))
